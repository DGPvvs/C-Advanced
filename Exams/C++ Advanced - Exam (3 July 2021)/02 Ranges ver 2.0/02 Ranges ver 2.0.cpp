int main()
{
	const std::string END = ".";

	std::map<int, int> startInterval = std::map<int, int>();
	std::map<int, int> endInterval = std::map<int, int>();

	bool isLoopExit = false;
	int id = 0;

	while (!isLoopExit)
	{
		myString input;
		std::getline(std::cin, input);

		if (input == END)
		{
			isLoopExit = true;
		}
		else
		{
			std::vector<int> range = input.ConvertStringToIntVector();

			startInterval[range[0]] = id;
			endInterval[range[1]] = id;

			id++;
		}
	}

	std::string output = std::string();

	isLoopExit = false;

	while (!isLoopExit)
	{
		myString input;
		std::getline(std::cin, input);

		if (input == END)
		{
			isLoopExit = true;
		}
		else
		{
			int num = stoi(input);

			std::map<int, int>::iterator start = startInterval.upper_bound(num);
			std::map<int, int>::iterator end = endInterval.lower_bound(num);
			bool isInRange = start != startInterval.begin() && end != endInterval.end() && ((--start)->second == end->second);

			if (isInRange)
			{
				output.append("in").append(myString().NewLine());
			}
			else				
			{
				output.append("out").append(myString().NewLine());
			}
		}
	}

	std::cout << output;
}

